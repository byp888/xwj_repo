package jre8Test;

public class Apple {
	
	private int weight;
	
	private String color;
	

	public Apple() {
	}

	public Apple(int weight) {
		super();
		this.weight = weight;
	}

	public int getWeight() {
		return weight;
	}

	public void setWeight(int weight) {
		this.weight = weight;
	}

	public String getColor() {
		return color;
	}

	public void setColor(String color) {
		this.color = color;
	}
	
}
